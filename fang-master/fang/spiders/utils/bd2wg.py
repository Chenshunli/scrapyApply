def convert_edge_2_84():
    json_file = json.load(open('../data/edge_poi_1.json', 'r'))
    for k, v in json_file.items():
        v.pop('geo')
        for point in v['points']:
            lng, lat = bd09_to_wgs84(point['x'], point['y'])
            point['x'], point['y'] = lng, lat

    json.dump(json_file, open('edge_poi_84.json', 'w'), ensure_ascii=False)


